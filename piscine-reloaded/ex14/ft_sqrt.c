int	ft_sqrt(int nb);

int	ft_sqrt(int nb)
{
	int	i;
	int	o;

	i = 0;
	o = 1;
	if (nb < 0)
		return (0);
	while (nb >= 0)
	{
		if (0 == nb)
			return (i);
		nb = nb - o;
		o += 2;
		i++;
	}
	return (0);
}
